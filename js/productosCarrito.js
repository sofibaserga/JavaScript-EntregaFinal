class ProductosCarrito {
    index;
    categoria;
    nombre;
    color;
    medidas;
    precio;
    cantidad;

    constructor(index, categoria, nombre, color, medidas, precio, cantidad) {
        this.index = index;
        this.categoria = categoria;  
        this.nombre = nombre;
        this.color = color;
        this.medidas = medidas;
        this.precio = precio;
        this.cantidad = cantidad;
    }
}